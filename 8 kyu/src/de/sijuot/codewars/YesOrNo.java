package de.sijuot.codewars;

public class YesOrNo {

    /**
     * Naja  gibt halt einen Boolean als String aus.
     * @param b Boolean
     * @return "Yes", wenn der Boolean true ist und "No", wenn er falsch ist.
     */
    public static String boolToWord(boolean b) {
        return b ? "Yes" : "No";
    }

}
